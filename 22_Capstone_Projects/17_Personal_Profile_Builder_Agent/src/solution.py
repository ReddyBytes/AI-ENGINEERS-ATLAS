def main():
    parser = argparse.ArgumentParser(
        description="Personal Profile Builder Agent — generate README, bio, and website"
    )
    parser.add_argument("username", help="GitHub username (e.g. torvalds)")
    parser.add_argument(
        "--linkedin", default="",
        help="LinkedIn public profile URL (optional)"
    )
    args = parser.parse_args()
    run(args.username, args.linkedin)
    print("\nDone. Open output/index.html in your browser.")


