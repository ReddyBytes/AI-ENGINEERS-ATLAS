# GitHub Scraper
# ---------------------------------------------------------------------------
def scrape_github(username: str) -> dict:
    """Fetch public GitHub profile data. Returns a flat dict."""
    try:
        from github import Github, GithubException   # ← pip install PyGithub
    except ImportError:
        raise ImportError("Install PyGithub: pip install PyGithub")

    g    = Github(GITHUB_TOKEN or None)              # ← None = anonymous (60 req/hr)
    user = g.get_user(username)

    # Top repos by star count (skip forks)
    all_repos = list(user.get_repos(type="owner", sort="updated"))
    top_repos = sorted(
        [r for r in all_repos if not r.fork],
        key=lambda r: r.stargazers_count,
        reverse=True,
    )[:5]

    repo_dicts = [
        {
            "name":        repo.name,
            "description": repo.description or "",
            "stars":       repo.stargazers_count,
            "language":    repo.language or "N/A",
            "url":         repo.html_url,
        }
        for repo in top_repos
    ]

    # Aggregate languages (sample first 20 non-fork repos)
    lang_counts: dict = {}
    for repo in [r for r in all_repos if not r.fork][:20]:
        try:
            for lang, byte_count in repo.get_languages().items():
                lang_counts[lang] = lang_counts.get(lang, 0) + byte_count
        except Exception:
            pass
    top_languages = sorted(lang_counts, key=lang_counts.get, reverse=True)[:5]  # type: ignore[arg-type]

    # Recent commits in last 30 days
    since = datetime.now(timezone.utc) - timedelta(days=30)
    recent_commits = 0
    for repo in all_repos[:10]:
        try:
            recent_commits += repo.get_commits(since=since, author=username).totalCount
        except Exception:
            pass

    return {
        "username":       username,
        "name":           user.name or username,
        "bio":            user.bio or "",
        "location":       user.location or "",
        "public_repos":   user.public_repos,
        "followers":      user.followers,
        "top_repos":      repo_dicts,
        "top_languages":  top_languages,
        "recent_commits": recent_commits,
        "github_url":     user.html_url,
    }


