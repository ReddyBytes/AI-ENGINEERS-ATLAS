# Output Generator
# ---------------------------------------------------------------------------
def generate_outputs(github_data: dict, profile: dict) -> dict:
    """Render Jinja2 templates and write output files. Returns {name: path}."""
    _ensure_templates()                             # ← write templates to disk if missing

    # Merge GitHub fields into profile dict for template access
    profile["github_username"] = github_data["username"]
    profile["github_url"]      = github_data["github_url"]
    profile["public_repos"]    = github_data["public_repos"]
    profile["followers"]       = github_data["followers"]
    profile["recent_commits"]  = github_data["recent_commits"]
    profile.setdefault("location", github_data.get("location", ""))
    profile.setdefault("name", github_data.get("name", github_data["username"]))

    env = Environment(
        loader=FileSystemLoader(str(TEMPLATES_DIR)),
        autoescape=False,                           # ← no HTML escaping in markdown templates
    )

    outputs = {}

    # profile_README.md
    readme = env.get_template("readme.md.j2").render(profile=profile)
    readme_path = OUTPUT_DIR / "profile_README.md"
    readme_path.write_text(readme, encoding="utf-8")
    outputs["readme"] = str(readme_path)

    # twitter_bio.txt
    bio = str(profile.get("twitter_bio", ""))[:160]  # ← hard cap at 160 chars
    bio_path = OUTPUT_DIR / "twitter_bio.txt"
    bio_path.write_text(bio, encoding="utf-8")
    outputs["twitter_bio"] = str(bio_path)

    # index.html
    html = env.get_template("index.html.j2").render(profile=profile)
    html_path = OUTPUT_DIR / "index.html"
    html_path.write_text(html, encoding="utf-8")
    outputs["html"] = str(html_path)

    return outputs


# ---------------------------------------------------------------------------
# Orchestrator
# ---------------------------------------------------------------------------
def run(github_username: str, linkedin_url: str = "") -> dict:
    """Full pipeline: scrape -> synthesise -> generate."""
    print(f"Fetching GitHub data for @{github_username} ...")
    github_data = scrape_github(github_username)
    print(f"  Name: {github_data['name']}")
    print(f"  Repos: {github_data['public_repos']} | "
          f"Followers: {github_data['followers']} | "
          f"Languages: {', '.join(github_data['top_languages'])}")

    print("Fetching LinkedIn data ...")
    linkedin_data = scrape_linkedin(linkedin_url)
    if linkedin_data["scraped"]:
        print(f"  Headline: {linkedin_data['headline']}")
    else:
        note = linkedin_data.get("note", "no URL provided")
        print(f"  LinkedIn not available ({note}) — using GitHub data only")

    print("Calling Claude to build profile ...")
    profile = build_profile(github_data, linkedin_data)
    print(f"  Summary: {str(profile.get('summary', ''))[:80]}...")
    print(f"  Skills: {', '.join(profile.get('skills', []))}")

    print("Generating output files ...")
    outputs = generate_outputs(github_data, profile)
    for name, path in outputs.items():
        print(f"  {name}: {path}")

    return outputs


